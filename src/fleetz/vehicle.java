    private void btnAddVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVehicleActionPerformed
        // TODO add your handling code here:
        if(BrandValidation(txtBrand.getText())&&ModelValidation(txtModel.getText())&&ChassisValidation(txtChassis.getText())&&RegValidation(txtRegno.getText())&&OdoValidation(txtOdometer.getText())&&TypeValidation(cmbType.getSelectedItem().toString())&&AvValidation(cmbAv.getSelectedItem().toString())){
        try{

            String sql="INSERT INTO vehicle (VID,Brand,Model,ChassisNo,Regno,Odometer,Type,Availability) VALUES (?,?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setInt(1,Integer.parseInt(txtVID.getText()));
            pst.setString(2,txtBrand.getText());
            pst.setString(3,(txtModel.getText()));
            pst.setString(4,txtChassis.getText());
            pst.setString(5,txtRegno.getText());
            pst.setDouble(6,Double.parseDouble(txtOdometer.getText()));
            pst.setString(7,cmbType.getSelectedItem().toString());
            pst.setString(8,cmbAv.getSelectedItem().toString());

            int i=pst.executeUpdate();

            if(i>0){

                JOptionPane.showMessageDialog(null,"Submitted");
                addID.setEnabled(true);
                btnUpdateVehicle.setEnabled(true);
                btnSearch.setEnabled(true);
                btnLoadVehicle.doClick();

            }
            else{
                JOptionPane.showMessageDialog(null,"Wrong input");
            }

        }
        catch(SQLException | NumberFormatException | HeadlessException e){
            JOptionPane.showMessageDialog(null,e);

        }
        }
        
        

    }//GEN-LAST:event_btnAddVehicleActionPerformed

