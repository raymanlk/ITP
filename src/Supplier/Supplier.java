    public void SupplierGetLoad(){
        
            String selectSql = "SELECT * " + "FROM supplier " + "WHERE SupplierID=?" ;

            try(PreparedStatement prep = connection.prepareStatement(selectSql)) {
                
                 prep.setString(1, (String) cbxSupID.getSelectedItem());
               
                
              ResultSet  rs=prep.executeQuery();
            while(rs.next())
               { 
               txtSupName1.setText(rs.getString(2));
               txtSupAddress.setText(rs.getString(3));
               txtSupTelephone.setText(rs.getString(4));
               txtSupEmail.setText(rs.getString(5));
               }
           }    
           
       catch (SQLException ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
      }
    
    
    
    
        
    
    
    
    
