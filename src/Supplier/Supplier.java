    public  void InsertSupplierDatabase() {
            
         
            // Prepared statement to insert data
                String insertSql = " INSERT INTO supplier ( Supplier_name , S_Address , Telpn , S_Email ) VALUES ( ? , ? , ? , ? ) " ;

               
                try (PreparedStatement prep = connection.prepareStatement(insertSql)) {
                        prep.setString(1, txtSupName1.getText());
                    
                        prep.setString(2, txtSupAddress.getText());
                        prep.setString(3, txtSupTelephone.getText());
                        prep.setString(4, txtSupEmail.getText());
                      

                         int i=prep.executeUpdate();
                         if(i>0)
                         {
                        JOptionPane.showMessageDialog(null,"Insert Complete");
                         }
                         else
                         {
                             JOptionPane.showMessageDialog(null,"error");
                         }
        }
        catch (Exception e) {
              JOptionPane.showMessageDialog(null,"This Data already in Database ","Error",JOptionPane.ERROR_MESSAGE);

           
        }
    
    }
    
    
    
    
    
    
    
