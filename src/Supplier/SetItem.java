      public  void InsertSETSupplierDatabase() {
            
         
            // Prepared statement to insert data
                String insertSql = " INSERT INTO setsupplieritem ( SupplierID , ItemCode ,ETIunit_price ,Type ) VALUES ( ? , ? , ? , ? ) " ;

               
                try (PreparedStatement prep = connection.prepareStatement(insertSql)) {
                        prep.setString(1, (String) cbxSupID.getSelectedItem());
                    
                        prep.setString(2, (String) cbxItemCode.getSelectedItem());
                         prep.setString(3, txtSupEsPrice.getText());
                         prep.setString(4, (String) cbxitemType.getSelectedItem());
                       
                      

                         int i=prep.executeUpdate();
                         if(i>0)
                         {
                        JOptionPane.showMessageDialog(null,"Insert Complete");
                         }
                         else
                         {
                             JOptionPane.showMessageDialog(null,"error");
                         }
        }
        catch (Exception e) {
              JOptionPane.showMessageDialog(null,"This Data already in Database ","Error",JOptionPane.ERROR_MESSAGE);

           
        }
    
    }
    
      
      
