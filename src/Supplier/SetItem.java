     public void SetTypeComboLoad(){
        
            String selectSql = "SELECT * " + "FROM item_inventory where Item_code = ? "  ;

            try(PreparedStatement prepSET = connection.prepareStatement(selectSql)){
                
                prepSET.setString(1, (String) cbxItemCode.getSelectedItem());
               boolean item;
               ResultSet rs= prepSET.executeQuery() ;
              
               while(rs.next())
               { 
                 
                item=(rs.getString(2)).matches("(.*)Bottle(.*)");
                if(item)
                {
                    cbxitemType.setSelectedIndex(2);
                }
               else 
                {
                    cbxitemType.setSelectedIndex(1);
                }
               
               }
           }    
           catch(Exception e)
           {
           }
 }
    
    
    
    
