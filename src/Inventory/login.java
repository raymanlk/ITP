    String user ;
    String passw ;
    public static boolean   usrPermission;
    
    
    public void Login(){
        
        
        
    
      
           
            
           
            // Create and execute a SELECT SQL statement.
            String Sql = "SELECT Password,Type FROM user WHERE UserName = ?"   ;

            try( PreparedStatement prep= connection.prepareStatement(Sql))  {
                        prep.setString(1,txtusername.getText() );
                        
                        
                         
                         
                 ResultSet  resultSet=prep.executeQuery();
                while(resultSet.next()){
                    
                    user=resultSet.getString(2);
                    passw=resultSet.getString(1);
                    
                }
                if(txtpwd.getText().equals(passw))
                {
                 if("admin".equals(user))
                         {
                           JOptionPane.showMessageDialog(null,"Admin");
                            usrPermission=true;
                            dispose();
                            main_menue news2= new main_menue();
                             news2.setVisible(true);
                         }
                         else if("staff".equals(user))
                             
                         {
                         JOptionPane.showMessageDialog(null,"Staff");
                            usrPermission=false;
                             dispose();
                            main_menue news2= new main_menue();
                             news2.setVisible(true);
                             
                         }

                }
                else 
                         {
                             JOptionPane.showMessageDialog(null,"Invalid user or Password");
                         }
              
         
                
                
            } catch (SQLException ex) {
            Logger.getLogger(Supplier.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    
    
