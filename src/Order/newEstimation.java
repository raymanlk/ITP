      public void calcEstimationAmount(){
          try{
               String productUnitPrice=jTextField5.getText();
               String productQty=jTextField4.getText();
               String productPaperType =(String) jComboBox7.getSelectedItem();
               String productPages= jTextField1.getText();
               String productSheets=jTextField2.getText();
               
                
               
              Statement stmt = connection.createStatement();
              rs=stmt.executeQuery("SELECT unitPrice from item_inventory where Item_name = '"+productPaperType+"' ");
              String paperPrice ="";
              
              while(rs.next()){
                  paperPrice = rs.getString("unitPrice");
              }
              
              double cUnitPrice     = Double.parseDouble(productUnitPrice);
              int    cQuantity      = Integer.parseInt(productQty);
              double cPaperPrice    = Double.parseDouble(paperPrice);
              int    cPages;
              int    cSheets;

              double amount=0;
              
             
              if((jTextField1.getText().isEmpty()) && (jTextField2.getText().isEmpty())){
                    amount = (cUnitPrice * cQuantity * cPaperPrice );
                   
                    
              }
              
              else if(jTextField1.getText().isEmpty()){
                    cSheets       = Integer.parseInt(productSheets);
                    amount = (cUnitPrice * cQuantity * cPaperPrice * cSheets );
                   
              }
              
              else if(productSheets.isEmpty()){
                    cPages         = Integer.parseInt(productPages);
                    amount = (cUnitPrice * cQuantity * cPaperPrice * cPages);
                   
              }
              
              else{
                   cPages         = Integer.parseInt(productPages);
                   cSheets        = Integer.parseInt(productSheets);
                   amount = (cUnitPrice * cQuantity * cPaperPrice * cPages * cSheets );
                   
              }
              jTextField13.setText(String.valueOf(amount));
              
          }
          catch(Exception e){
           }
      }
     
   

