    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int q = jTable1.getSelectedRow();
        String order = jTable1.getValueAt(q, 0).toString();
        String name="";
        try{
            Statement stmt = connection.createStatement();
            rs = stmt.executeQuery("select o.Orderid,c.Cname from  customer c, ordercust o where o.Orderid = '"+order+"' and o.Cid=c.CID ");
            
            while(rs.next()){
                order = rs.getString("o.Orderid");
                jTextField8.setText(order);
                
                name = rs.getString("c.Cname");
                jTextField9.setText(name);
            }
        }
        catch(SQLException e){
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

